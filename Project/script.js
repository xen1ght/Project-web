document.addEventListener("DOMContentLoaded", () => {
    const langLinks = document.querySelectorAll(".header__lang-link");
    const title = document.querySelector(".lead__title");
    const subtitle = document.querySelector(".lead__subtitle");
    const caption = document.querySelector(".lead__caption");
    const introTitle = document.querySelector(".intro__title");
    const introText = document.querySelector(".intro__text");
    const introList = document.querySelector(".intro__list");
    const places = document.querySelectorAll(".place");
    const photoGridItems = document.querySelectorAll(".photo-grid__item");
    const footerLinks = document.querySelectorAll(".footer__link");
    const coverTitle = document.querySelector(".cover__title");  
    const coverSubtitle = document.querySelector(".cover__subtitle");  

    langLinks.forEach(link => {
        link.addEventListener("click", (e) => {
            e.preventDefault();
            const selectedLang = link.textContent.trim().toLowerCase();

        
            title.textContent = translations[selectedLang].leadTitle;
            subtitle.textContent = translations[selectedLang].leadSubtitle;
            caption.textContent = translations[selectedLang].leadCaption;
            introTitle.textContent = translations[selectedLang].introTitle;
            introText.textContent = translations[selectedLang].introText;

           
            introList.innerHTML = translations[selectedLang].intro__list.map(item => `<li>${item}</li>`).join('');

       
            Object.keys(translations[selectedLang].places).forEach((placeId, index) => {
                const place = translations[selectedLang].places[placeId];
                const placeTitle = document.getElementById(`place__title-${index + 1}`);
                const placeParagraphs = document.querySelectorAll(`#place__paragraph-${index * 2 + 1}, #place__paragraph-${index * 2 + 2}`);

                if (placeTitle) {
                    placeTitle.textContent = place.title;
                }
                placeParagraphs.forEach((paragraph, i) => {
                    paragraph.textContent = place[`paragraph${i + 1}`];
                });
            });

        
            if (coverTitle) coverTitle.textContent = translations[selectedLang].coverTitle;
            if (coverSubtitle) coverSubtitle.textContent = translations[selectedLang].coverSubtitle;

           
            photoGridItems.forEach((item, index) => {
                item.alt = translations[selectedLang].photoGridAlts ? translations[selectedLang].photoGridAlts[index] : item.alt;
            });

            
            footerLinks.forEach((link, index) => {
                link.textContent = translations[selectedLang].footerLinks[index];
            });

            
            langLinks.forEach(l => l.classList.remove("header__lang-link_active"));
            link.classList.add("header__lang-link_active");
        });
    });
});
